package labs;

public class MinMax {
	
	public static void main(String [] args) {
		
	int [] test = {1,2,3,4,5,6,7,8};
	
	int temp=0;
	int temp2 =20000000;
	
	for (int i : test) {
		
		if(i > temp){
			
			temp = i;
		}
	}
	for (int i : test) {
		if(i< temp2){
			
			temp2 = i;
		}
	}
		
	System.out.println("" + temp +" " + temp2);
	
	
	}
	
	
}
